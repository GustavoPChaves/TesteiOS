import Foundation
import UIKit

extension UIColor{
    class var loginButtonShadow: UIColor{
        return UIColor(red: 59/255, green: 72/255, blue: 238/255, alpha: 1)
    }
    class var loginButton: UIColor{
        return UIColor(red: 59/255, green: 72/255, blue: 238/255, alpha: 1)
    }
    class var customGray: UIColor{
        return UIColor(red: 72/255, green: 84/255, blue: 101/255, alpha: 1)
    }
    class var customBlue: UIColor{
        return  UIColor(red: 59/255, green: 73/255, blue: 238/255, alpha: 1)
    }
    class var borderColor: UIColor{
        return   UIColor(red: 219/255, green: 223/255, blue: 227/255, alpha: 0.3)
    }
  
}

